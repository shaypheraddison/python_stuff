# convert a string to an int
convert_str_int = int("9000")
print(convert_str_int)

# convert an int to a string
convert_int_str = str(44)
print(type(convert_int_str))

# convert int to a float
convert_int_float = float(101)
print(convert_int_float)

# convert float to an int
convert_float_int = int(75.4)
print(convert_float_int)

# convert a string to a list
convert_str_list = list("Goodbye")
print(convert_str_list)

# convert a list to a tuple
convert_list_tuple = tuple([1, 2, 3])
print(type(convert_list_tuple))
